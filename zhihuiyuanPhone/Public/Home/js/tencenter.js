function worda (){
	var oIn = document.getElementById("ten-word-a");

	oIn.addEventListener('touchstart',touch,false);
	oIn.addEventListener('touchend',touch,false);


	function touch(event){
	var event = event || window.event;
         
    var oInp = document.getElementById('ten-word-a');
    switch(event.type){
            case "touchstart":
            oInp.style.top="-100%";
            break;
            case "touchend":
            oInp.style.top="0";
        }
    }
}
